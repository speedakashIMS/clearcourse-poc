#!/usr/bin/env node

/**
 * Script to process CSS variables from design-tokens.css and update index.css
 *
 * This script:
 * 1. Reads CSS variables for Desktop/Tablet/Mobile from design-tokens.css
 * 2. Transforms variables into Tailwind theme tokens (colors, spacing, font sizes)
 * 3. Writes base (mobile) tokens into @theme in index.css
 * 4. Writes responsive overrides for tablet/desktop as :root media queries
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const BREAKPOINTS = {
  tablet: '1024px',
  desktop: '1280px',
};

const BASE_COMMENT = '/* Autogenerated from CSS variables - DO NOT EDIT MANUALLY */';
const RESPONSIVE_START = '/* Autogenerated responsive overrides - DO NOT EDIT MANUALLY */';
const RESPONSIVE_END = '/* End responsive overrides */';

function parseVariablesFromRoot(rootContent) {
  const variables = {};
  const varPattern = /--([\w-]+):\s*([^;]+);/g;
  let match;

  while ((match = varPattern.exec(rootContent)) !== null) {
    const varName = `--${match[1]}`;
    const value = match[2].trim();
    variables[varName] = value;
  }

  return variables;
}

/**
 * Match :root blocks whose preceding comment ends with "- Desktop", "- Tablet", or "- Mobile".
 * Comment prefix may vary (e.g. "CC Foundation", "Design Tokens", etc.).
 */
function parseCSSGroups(cssContent) {
  const groups = {};
  const groupPattern = /\/\*[\s\S]*?-\s*(Desktop|Tablet|Mobile)\s*\*\/\s*:root\s*\{([\s\S]*?)\}/g;
  let match;

  while ((match = groupPattern.exec(cssContent)) !== null) {
    const groupName = match[1].toLowerCase();
    const rootContent = match[2];
    groups[groupName] = parseVariablesFromRoot(rootContent);
  }

  return groups;
}

/**
 * Transform color key:
 * - "--Colours-Placeholder-Grey-50" -> "grey-50"
 * - "--Colours-Brand-White" -> "white"
 * - "--Colours-Brand-Primary" -> "brand-primary"
 */
function transformColorKey(varName) {
  let key = varName.replace(/^--Colours-/, '');

  if (key.startsWith('Placeholder-Grey-')) {
    key = key.replace('Placeholder-Grey-', 'Grey-');
  }

  if (key === 'Brand-White') {
    key = 'White';
  }

  return key.toLowerCase();
}

/**
 * Transform spacing unit key:
 * "--Units-Unit-24" -> "24"
 */
function transformSpacingKey(varName) {
  const key = varName.replace(/^--Units-Unit-/, '');
  return key.toLowerCase();
}

/**
 * Transform component spacing key:
 * "--Cards-image--padding-x" -> "cards-image--padding-x"
 */
function transformComponentSpacingKey(varName) {
  const key = varName.replace(/^--/, '');
  return key.toLowerCase();
}

/**
 * Transform typography key:
 * "--Typography-Display-Display-XL" -> "display-xl"
 * "--Typography-Body-Body-Large" -> "body-large"
 */
function transformTypographyKey(varName) {
  let key = varName.replace(/^--Typography-/, '').replace(/---LH$/, '');
  key = key.replace(/^(Display|Body|Headings|UI|Buttons)-(\1)-/i, '$1-');
  return key.toLowerCase();
}

function resolveUnitReferences(value, variables) {
  return value.replace(/var\(--Units-Unit-(\d+)\)/g, (match, unit) => {
    const unitVar = `--Units-Unit-${unit}`;
    return variables[unitVar] || match;
  });
}

function roundPxValue(value) {
  return value.replace(/(-?\d+(?:\.\d+)?)px/g, (match, number) => {
    const rounded = Math.round(parseFloat(number) * 10) / 10;
    return `${rounded}px`;
  });
}

/**
 * Process variables into Tailwind theme tokens.
 */
function processCSSVariables(variables) {
  const themeVars = [];
  const fontPairs = {};

  Object.entries(variables).forEach(([varName, value]) => {
    if (varName.startsWith('--Colours-')) {
      const key = transformColorKey(varName);
      themeVars.push(`  --color-${key}: ${value};`);
      return;
    }

    if (varName.startsWith('--Units-Unit-')) {
      const key = transformSpacingKey(varName);
      themeVars.push(`  ${varName}: ${value};`);
      themeVars.push(`  --spacing-${key}: ${value};`);
      themeVars.push(`  --radius-${key}: ${value};`);
      themeVars.push(`  --space-y-${key}: ${value};`);
      themeVars.push(`  --space-x-${key}: ${value};`);
      themeVars.push(`  --gap-${key}: ${value};`);
      return;
    }

    if (varName.startsWith('--Typography-')) {
      const key = transformTypographyKey(varName);
      if (!fontPairs[key]) {
        fontPairs[key] = {};
      }
      const normalizedValue = roundPxValue(value);
      if (varName.endsWith('---LH')) {
        fontPairs[key].lineHeight = normalizedValue;
      } else {
        fontPairs[key].size = normalizedValue;
      }
      return;
    }

    const key = transformComponentSpacingKey(varName);
    const resolvedValue = resolveUnitReferences(value, variables);
    themeVars.push(`  --spacing-${key}: ${resolvedValue};`);
  });

  Object.entries(fontPairs).forEach(([key, pair]) => {
    if (pair.size) {
      themeVars.push(`  --font-size-${key}: ${pair.size};`);
    }
    if (pair.lineHeight) {
      themeVars.push(`  --line-height-${key}: ${pair.lineHeight};`);
    }
  });

  return themeVars.sort((a, b) => a.localeCompare(b));
}

function findAutogeneratedSection(cssContent) {
  const commentIndex = cssContent.indexOf(BASE_COMMENT);
  if (commentIndex === -1) return null;

  const themeStart = cssContent.lastIndexOf('@theme', commentIndex);
  if (themeStart === -1) return null;

  let braceIndex = themeStart + '@theme'.length;
  while (braceIndex < cssContent.length && /\s/.test(cssContent[braceIndex])) {
    braceIndex++;
  }

  if (cssContent[braceIndex] !== '{') return null;

  let braceCount = 1;
  let themeEnd = braceIndex + 1;
  while (themeEnd < cssContent.length && braceCount > 0) {
    if (cssContent[themeEnd] === '{') braceCount++;
    else if (cssContent[themeEnd] === '}') braceCount--;
    themeEnd++;
  }

  let sectionEnd = commentIndex + BASE_COMMENT.length;
  while (sectionEnd < themeEnd && cssContent[sectionEnd] !== '}') {
    sectionEnd++;
  }

  return {
    commentStart: commentIndex,
    sectionEnd,
  };
}

function replaceResponsiveSection(cssContent, responsiveContent) {
  const startIndex = cssContent.indexOf(RESPONSIVE_START);
  const endIndex = cssContent.indexOf(RESPONSIVE_END);

  if (startIndex !== -1 && endIndex !== -1 && endIndex > startIndex) {
    const before = cssContent.slice(0, startIndex);
    const after = cssContent.slice(endIndex + RESPONSIVE_END.length);
    return `${before}${responsiveContent}${after}`;
  }

  return `${cssContent}\n\n${responsiveContent}`;
}

function updateIndexCSS({ baseVars, tabletVars, desktopVars }) {
  const cssPath = path.join(rootDir, 'src', 'index.css');

  if (!fs.existsSync(cssPath)) {
    console.error(`Error: src/index.css not found at ${cssPath}`);
    process.exit(1);
  }

  let cssContent = fs.readFileSync(cssPath, 'utf-8');
  const section = findAutogeneratedSection(cssContent);

  const baseSection = `  ${BASE_COMMENT}\n${baseVars.join('\n')}\n`;

  if (section) {
    const beforeSection = cssContent.slice(0, section.commentStart);
    const afterSection = cssContent.slice(section.sectionEnd);
    cssContent = beforeSection + baseSection + afterSection;
  } else {
    const themeMatch = cssContent.match(/(@theme\s*\{)/);
    if (!themeMatch) {
      console.error('Error: Could not find @theme block in index.css');
      process.exit(1);
    }
    const insertPosition = themeMatch.index + themeMatch[0].length;
    cssContent =
      cssContent.slice(0, insertPosition) +
      `\n${baseSection}` +
      cssContent.slice(insertPosition);
  }

  const responsiveSection = [
    RESPONSIVE_START,
    `@media (min-width: ${BREAKPOINTS.tablet}) {`,
    '  :root {',
    tabletVars.join('\n'),
    '  }',
    '}',
    `@media (min-width: ${BREAKPOINTS.desktop}) {`,
    '  :root {',
    desktopVars.join('\n'),
    '  }',
    '}',
    RESPONSIVE_END,
  ].join('\n');

  cssContent = replaceResponsiveSection(cssContent, responsiveSection);
  fs.writeFileSync(cssPath, cssContent, 'utf-8');

  console.log('✅ Updated src/index.css with theme variables');
}

try {
  console.log('Building theme variables from design-tokens.css...\n');

  const cssFilePath = path.join(rootDir, 'design-tokens.css');
  if (!fs.existsSync(cssFilePath)) {
    console.error(`Error: design-tokens.css not found at ${cssFilePath}`);
    process.exit(1);
  }

  const cssContent = fs.readFileSync(cssFilePath, 'utf-8');
  const groups = parseCSSGroups(cssContent);

  if (!groups.mobile || !groups.tablet || !groups.desktop) {
    console.error('Error: Expected Desktop, Tablet, and Mobile groups in design-tokens.css');
    process.exit(1);
  }

  const baseVars = processCSSVariables(groups.mobile);
  const tabletVars = processCSSVariables(groups.tablet);
  const desktopVars = processCSSVariables(groups.desktop);

  updateIndexCSS({ baseVars, tabletVars, desktopVars });

  console.log('\n✅ Processing complete!');
  console.log(`   - ${baseVars.length} base variables generated`);
  console.log(`   - ${tabletVars.length} tablet overrides generated`);
  console.log(`   - ${desktopVars.length} desktop overrides generated`);
  console.log(`   - Breakpoints: tablet ${BREAKPOINTS.tablet}, desktop ${BREAKPOINTS.desktop}`);
} catch (error) {
  console.error('Error:', error.message);
  console.error(error.stack);
  process.exit(1);
}

